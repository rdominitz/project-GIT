using System;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Server;
using Entities;
using Constants;
using System.Collections.Generic;

namespace ServerLogicTests
{
    [TestClass]
    public class GetAutoGeneratedQuestion
    {
        private IServer _server;

        [TestInitialize]
        public void TestInitialize()
        {
            _server = new ServerImpl(new FakeMedTrainDBContext());
            _server.login("defaultadmin@gmail.com", "password");
            _server.addSubject(Users.USER_UNIQUE_INT, "subject");
            _server.addTopic(Users.USER_UNIQUE_INT, "subject", "topic");
        }

        [TestMethod]
        public void getAutoGeneratedQuestionUserNotLoggedIn()
        {
            int userId = _server.register("user@gmail.com", "password", Users.medicalTrainingLevels[0], "fn", "ln").Item2;
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(userId, "subject", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionNonExistingSubject()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "no_subject", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionNonExistingTopic()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", "no_topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionNoQuestions()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionWrongUserId()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(99999, "subject", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionSubjectIsNull()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, null, "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionSubjectIsNullString()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "null", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionSubjectIsEmpty()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "", "topic").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionTopicIsNull()
        {
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", null).Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionTopicIsNullString()
        {
            _server.login("defaultadmin@gmail.com", "password");
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", "null").Equals(Replies.SUCCESS));
        }

        [TestMethod]
        public void getAutoGeneratedQuestionTopicIsEmpty()
        {
            _server.login("defaultadmin@gmail.com", "password");
            Assert.IsFalse(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", "").Equals(Replies.SUCCESS));
        }
        
        [TestMethod]
        public void getAutoGeneratedQuestionSuccessfully()
        {
            _server.createQuestion(Users.USER_UNIQUE_INT, "subject", new List<string>() { "topic" }, new List<byte[]>() { new byte[1] { 5 } }, "");
            Assert.IsTrue(_server.getAutoGeneratedQuesstion(Users.USER_UNIQUE_INT, "subject", "topic").Equals(Replies.SUCCESS));
        }
    }
}
